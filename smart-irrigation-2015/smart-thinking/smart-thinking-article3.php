                        <p>Being smart about irrigation is a good idea, both for the environment and your back pocket. Up to 50 per cent of all water used in the home is for watering landscapes with the average property owner over-watering by 38 per cent. The good news is that it&rsquo;s also easy to be smart when it comes to irrigation thanks to new developments in irrigation technology.</p>
                        <p>The conventional controllers are typically timed based and rely on the user to determine how often to irrigate, resulting in excessive amounts of water applied to the garden. Additionally, once they are programmed, they are rarely changed so weather and other conditions are not factored in.</p>
                        <p>Smart Irrigation systems can reduce water bills significantly. Whether you are an irrigation installer, landscaper, maintenance worker or a home owner, systems are affordable, save precious water resources and keep landscapes in peak condition.  Smart Irrigation systems enable weather-based watering which take into account seasonal, soil, plant and weather conditions to reduce over-watering, providing optimal moisture to the landscape.</p>
                        <h5>1.  Save Time &ndash; The system does all of the work</h5>
                        <p>Gone are the days where time is wasted with a hose in hand watering the lawn, plants and flowers. Now, with a Smart Irrigation System, timers are set so that watering occurs only when it is best for the landscape and climate the landscape is located.  Home owners can go out on a hot day or away on holidays with confidence that their landscape will be maintained and that their lawns and plants will be looked after and blossoming on return.</p>
                        <h5>2.  Save water &ndash; The system is much more efficient than traditional time based systems</h5>
                        <p>The Weathermatic SmartLink system can save as much as 20-50% of the water that would normally be used with a typical irrigation system. The systems can be extremely efficient and will only apply the amount of water required by the plants. There is little to no waste through overwatering, overspray and run off.</p>
                        <h5>3.  Save money &ndash; Less water means lower cost</h5>
                        <p>With everything programmed to suit each landscape, climate and weather conditions, the landscape will only be watered when required. No water wasted means no money wasted. Additional costs including replacing dead plants that may occur with traditional watering methods are also prevented. It also decreases the taxes needed to build more water infrastructure that may be caused through run off and erosion.</p>
                        <h5>4.  Save the plants &ndash; plants will be kept in peak health</h5>
                        <p>With optimal watering plants are kept in perfect health and growth is promoted. Plants and lawn will grow a lot faster when they are watered correctly over a long period of time. More luscious, greener and healthier landscapes can be achieved.</p> 
