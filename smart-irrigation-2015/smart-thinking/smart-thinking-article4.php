                        <h5>What is dripline?</h5>
                        <p>Drip irrigation is an efficient and economical method of watering. Drip irrigation involves placing tubes with emitters in the garden bed or ground alongside the plants. Water is slowly dripped through the emitters directly into the soil at the root zone. Because moisture levels are kept at an optimal range, plant productivity and quality improve. </p>
                        <h5>Drip line is good for use:</h5>
                        <li>In a small yard</li>
                        <li>For individual plants</li>
                        <li>In mulched areas so that water can soak straight into the soil without washing away mulch</li>
                        <li>In sloped areas. Quick absorption reduces the likeliness of run off</li>
                        <h5>Benefits:</h5>
                        <li>When programmed correctly; drip line can save water</li>
                        <li>Minimal evaporation of water due to the line being under the mulch</li>
                        <li>Water is quickly absorbed</li>
                        <li>Reduces weed growth</li>
                        <li>Wind drift and overspray issues are eliminated</li>
