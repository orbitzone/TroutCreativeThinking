                        <p>Many irrigation systems are not designed and set up according to the type of plant and its requirements. Unfortunately, a lot of irrigation systems tend to water turf and every type of plant in the landscape at the same time and rate. This method is inefﬁcient and can cause waster wastage.</p>
                        <p>A hydrozone is a fantastic way to design an irrigation system. It involves establishing separate areas within the landscape where plants are grouped together based on their water, soil and sun requirements. Preferably, each zone of the irrigation system will supply the plants with the same water requirements with a suitable amount of water.  For example one group of plants may need watering for ten minutes while another section of the landscape may require twenty minutes. Generally turf areas and garden beds have different water needs and should be separated.</p>
                        <p>Hydrozoning can also take climate factors into consideration. As an example, sensitive plants would be best positioned in the shade whereas plants that can tolerate more heat and/or wind might be better off positioned by the road. </p>
                        <h5>Why use Hydrozones?</h5>
                        <p>Hydrozones are intended to encourage effective designs and allow landscapes to conserve water. Hydrozoning simpliﬁes the irrigation scheduling and also ensures each plant is receiving the optimal amount of moisture leading to a better landscape performance. This therefore saves water, money and also time.</p>
                        <p>Finding the correct plant information, designing and applying hydrozones can be quite difﬁcult and complex. A professional consultation is recommended.  A landscape professional can help property owners decide how to shift to this type of planting method.</p>

