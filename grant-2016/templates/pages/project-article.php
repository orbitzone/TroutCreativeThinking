					      <p><strong>In Haiti only 70 per cent of the population has access to safe water resources and fewer than 20 per cent have access to adequate sanitation.</strong></p>
								<p>Over 100 experts gathered in Haiti at the end of May to work on an urgent action plan for implementing a national sanitation strategy. The move to improve sanitation systems and behaviours in Haiti has been put on ‘urgent’ due to a number of reasons, the following <br> being paramount.</p>
								<p><strong>1 in 4 people defecate in open in rural areas</strong></p>
								<p>Much of the rural population in Haiti does not use a toilet even if they have one. Those who do have a toilet often save them for guest-use only. That means the stakes are pretty high – especially as waterborne diseases (caused by poor hygiene and sanitation) are responsible for 20 per cent of deaths in children under five in Haiti.</p>
								<p><strong>Public toilets and structures are difficult to use</strong></p>
								<p>Haiti is currently experiencing large population growth, with a 57 per cent urbanisation rate in 2014 that’s tipped to reach 76 per cent in 2050. Sanitation efforts tend to be difficult for workers, like merchants of fruit and vegetables who spend eight and more hours a day in markets making access to adequate latrines difficult.</p>
								<p>The situation doesn’t get much better in schools where sanitation infrastructure is generally poorly maintained and unused.</p>
								<p><strong>Haiti’s three stages of sanitation – toilets, evacuation and treatment – are inefficient</strong></p>
								<p>Haiti has virtually no viable systems for collection and treatment of waste water. The bayakous, or septic tank cleaners, go into the septic tanks without protection, using only wheelbarrows to dispose of sludge.</p>
								<p>Meanwhile a majority of contaminated water is poured unchecked into the natural environment. With just one treatment site in operation and five other centres under construction, the situation can only improve.</p>
								<p>“Improving sanitation is everybody’s business,” said Mary Barton-Dock, World Bank Special Envoy in Haiti. “Each toilet built, adopted and used, means that one family won’t be affected by cholera and other waterborne diseases that cause the death of more children under five years than HIV/AIDS, malaria and measles combined.</p>
								<p>“We cannot ignore the possibility of saving lives by improving access to sanitation and supporting this new national roadmap,” she said.</p>
